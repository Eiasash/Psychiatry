#!/usr/bin/env node
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const html = readFileSync(resolve(here, "../index.html"), "utf8");
const failures = [];

function expect(pattern, label) {
  if (!pattern.test(html)) failures.push(label);
}

expect(/@media\(max-width:560px\)\{[\s\S]*?\.fb\.show\{[^}]*margin-inline:-4px/, "mobile feedback should reclaim horizontal space");
expect(/@media\(max-width:560px\)\{[\s\S]*?\.ai-box\{[^}]*border-left:0[^}]*border-right:0/, "mobile AI panel should avoid nested side rails");
expect(/@media\(max-width:560px\)\{[\s\S]*?\.ai-section\{[^}]*padding:10px[^}]*margin:8px 0/, "mobile AI sections should be compact, separated cards");
expect(/@media\(max-width:560px\)\{[\s\S]*?\.ai-table td::before\{[^}]*data-label/, "mobile AI tables should expose per-cell labels");
expect(/\.ai-loading\{[\s\S]*?border:1px solid var\(--line\)/, "AI loading state should be visibly framed");
expect(/\.ai-error\{[\s\S]*?border:1px solid var\(--bad\)/, "AI error state should be visibly framed");
expect(/\.update-banner\{[\s\S]*?max-width:720px/, "update banner should have a stable max width");

if (failures.length) {
  console.error(`Mobile visual contract failed (${failures.length}):`);
  for (const failure of failures) console.error(`  - ${failure}`);
  process.exit(1);
}

const capture = process.argv.includes("--capture");
if (!capture) {
  console.log("Mobile visual contract passed. Run with --capture in an environment with Playwright to write screenshots.");
  process.exit(0);
}

let chromium;
try {
  ({ chromium } = await import("playwright"));
} catch {
  console.log("Mobile visual contract passed. Playwright is not installed, so screenshot capture was skipped.");
  process.exit(0);
}

const outDir = resolve(here, "../results/mobile-visual");
mkdirSync(outDir, { recursive: true });
const browser = await chromium.launch();
const page = await browser.newPage({ viewport: { width: 393, height: 873 }, deviceScaleFactor: 2, isMobile: true });
await page.goto(`file://${resolve(here, "../index.html").replaceAll("\\", "/")}`, { waitUntil: "domcontentloaded" });
await page.screenshot({ path: resolve(outDir, "home-mobile.png"), fullPage: true });
writeFileSync(resolve(outDir, "README.txt"), "Generated by npm run visual:mobile -- --capture\n", "utf8");
await browser.close();
console.log(`Mobile screenshots written to ${outDir}`);
